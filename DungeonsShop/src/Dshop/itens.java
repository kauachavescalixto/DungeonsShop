package Dshop;

public class itens {
	private String nome, comum, raro, unico, arma, armadura, preco;
	private int id;
	
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getComum() {
		return comum;
	}

	public void setComum(String comum) {
		this.comum = comum;
	}
	
	public String getRaro() {
		return raro;
	}

	public void setRaro(String raro) {
		this.raro = raro;
	}
	
	public String getUnico() {
		return unico;
	}

	public void setUnico(String unico) {
		this.unico = unico;
	}
	
	public String getArma() {
		return arma;
	}

	public void setArma(String arma) {
		this.arma = arma;
	}
	
	public String getArmadura() {
		return armadura;
	}

	public void setArmadura(String armadura) {
		this.armadura = armadura;
	}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	public String getPreco() {
		return preco;
	}

	public void setPreco(String preco) {
		this.preco = preco;
	}


}